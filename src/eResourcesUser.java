    public void fetchResources() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Очистить таблицу

        try (Connection con = ConnectionProvider.getCon()) {
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT * FROM e_resources");

            while (rs.next()) {
                String id = rs.getString("resourceID");
                String title = rs.getString("title");
                String url = rs.getString("url");
                String description = rs.getString("description");
                String category = rs.getString("category");

                model.addRow(new Object[]{id, title, url, description, category});
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Ошибка при получении ресурсов: " + e.getMessage());
            e.printStackTrace();
        }
    }
    
    public class URLCellRenderer extends DefaultTableCellRenderer {
    public Component getTableCellRendererComponent(JTable table, Object value,
                                                   boolean isSelected, boolean hasFocus,
                                                   int row, int column) {
        super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
        if (value instanceof String) {
            setText("<html><a href=\"\">" + value.toString() + "</a></html>");
        }
        setForeground(Color.BLUE);
        return this;
    }
}
    
